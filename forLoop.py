# looping -> perulangan

# for kondisi :
#     aksi

# loop dengan list
angka1 = [1,2,3,4,5]

for i in angka1 :
    print(f"i sekarang -> {i}")
print("\n")

# loop dengan range

range1 = range(6)

for i in range1 :
    print(f"i sekarang -> {i}")
print("\n")

range2 = range(5,10)

for i in range2 :
    print(f"i sekarang -> {i}")
print("\n")

# loop dengan string
string = "jooss jiiiss"

for i in string :
    print(i)